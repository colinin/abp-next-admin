namespace LINGYUN.Linq.Dynamic.Queryable;
public enum DynamicComparison
{
    /// <summary>
    /// 等于
    /// </summary>
    Equal = 0,
    /// <summary>
    /// 不等于
    /// </summary>
    NotEqual = 1,
    /// <summary>
    /// 小于
    /// </summary>
    LessThan = 2,
    /// <summary>
    /// 小于等于
    /// </summary>
    LessThanOrEqual = 3,
    /// <summary>
    /// 大于
    /// </summary>
    GreaterThan = 4,
    /// <summary>
    /// 大于等于
    /// </summary>
    GreaterThanOrEqual = 5,
    /// <summary>
    /// 左包含
    /// </summary>
    StartsWith = 6,
    /// <summary>
    /// 左不包含
    /// </summary>
    NotStartsWith = 7,
    /// <summary>
    /// 右包含
    /// </summary>
    EndsWith = 8,
    /// <summary>
    /// 右不包含
    /// </summary>
    NotEndsWith = 9,
    /// <summary>
    /// 包含
    /// </summary>
    Contains = 10,
    /// <summary>
    /// 不包含
    /// </summary>
    NotContains = 11,
    /// <summary>
    /// 空值
    /// </summary>
    Null = 12,
    /// <summary>
    /// 非空
    /// </summary>
    NotNull = 13
}

